package com.app.beans;

public class board {
	private String board_index;
	private String user_name;
	private String user_id;
	private String board_title;
	private String board_body;
	private String board_bodyType;
	private String board_file;
	private String date;

	public String getBoard_index() {
		return board_index;
	}
	public void setBoard_index(String board_index) {
		this.board_index = board_index;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getBoard_title() {
		return board_title;
	}
	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}
	public String getBoard_body() {
		return board_body;
	}
	public void setBoard_body(String board_body) {
		this.board_body = board_body;
	}
	public String getBoard_bodyType() {
		return board_bodyType;
	}
	public void setBoard_bodyType(String board_bodyType) {
		this.board_bodyType = board_bodyType;
	}
	public String getBoard_file() {
		return board_file;
	}
	public void setBoard_file(String board_file) {
		this.board_file = board_file;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
